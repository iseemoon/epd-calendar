const char * get_day_of_solar_term(uint32_t y, uint8_t n, DATE * solar_date)
{
    if (y >= 1900 && y < 2100 && n < 24)
    {
        SOLAR_TERM_TIME * item = &SOLAR_TERM_TIMES[y - 1900];
        time_t t = item->times[n] / 1000;
        struct tm * date = localtime(&t);
        solar_date->year = date->tm_year + 1900;
        solar_date->month = date->tm_mon + 1;
        solar_date->day   = date->tm_mday;
        return SOLAR_TERM_NAME[n];
    }
    return NULL;
}
